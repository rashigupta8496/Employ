
public class Emp {

	private int roll,ten,sal;
	private String name;
	Emp(int r,String n,int t,int s)
	{
		this.roll=r;
		this.name=n;
		this.ten=t;
		this.sal=s;
	}
	public int getRoll() {
		return roll;
	}
	public int getTen() {
		return ten;
	}
	public int getSal() {
		return sal;
	}
	public String getName() {
		return name;
	}
	
}
